#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	k;

	i = 0;
	k = 1;
	if (argc > 1)
	{
		while (argc > k)
		{
			i = 0;
			while (argv[k][i] != '\0')
			{
				write(1, &argv[k][i], 1);
				i++;
			}
			write (1, "\n", 1);
			k++;
		}
	}
	return (0);
}

#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_reverse_alphabet(void)
{
	char	ters_alfabe;

	ters_alfabe = 'z';
	while (ters_alfabe >= 'a')
	{
		ft_putchar(ters_alfabe);
		ters_alfabe--;
	}
}
